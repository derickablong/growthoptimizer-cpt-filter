<?php

final class GO_2C_PHOTO_ARTICLE {

	/**
	 * Plugin Version
	 *
	 * @since 1.0.0
	 *
	 * @var string The plugin version.
	 */
	const VERSION = '1.0.0';

	/**
	 * Minimum Elementor Version
	 *
	 * @since 1.0.0
	 *
	 * @var string Minimum Elementor version required to run the plugin.
	 */
	const MINIMUM_ELEMENTOR_VERSION = '2.0.0';

	/**
	 * Minimum PHP Version
	 *
	 * @since 1.0.0
	 *
	 * @var string Minimum PHP version required to run the plugin.
	 */
	const MINIMUM_PHP_VERSION = '7.0';

	/**
	 * Instance
	 *
	 * @since 1.0.0
	 *
	 * @access private
	 * @static
	 *
	 * @var Elementor_Test_Extension The single instance of the class.
	 */
	private static $_instance = null;

	/**
	 * Instance
	 *
	 * Ensures only one instance of the class is loaded or can be loaded.
	 *
	 * @since 1.0.0
	 *
	 * @access public
	 * @static
	 *
	 * @return Elementor_Test_Extension An instance of the class.
	 */
	public static function instance() {

		if ( is_null( self::$_instance ) ) {
			self::$_instance = new self();
		}
		return self::$_instance;

	}

	/**
	 * Constructor
	 *
	 * @since 1.0.0
	 *
	 * @access public
	 */
	public function __construct() {

		add_action( 'init', [ $this, 'i18n' ] );
		add_action( 'plugins_loaded', [ $this, 'init' ] );

	}

	/**
	 * Load Textdomain
	 *
	 * Load plugin localization files.
	 *
	 * Fired by `init` action hook.
	 *
	 * @since 1.0.0
	 *
	 * @access public
	 */
	public function i18n() { load_plugin_textdomain( 'elementor-test-extension' ); }

	/**
	 * Initialize the plugin
	 *
	 * Load the plugin only after Elementor (and other plugins) are loaded.
	 * Checks for basic plugin requirements, if one check fail don't continue,
	 * if all check have passed load the files required to run the plugin.
	 *
	 * Fired by `plugins_loaded` action hook.
	 *
	 * @since 1.0.0
	 *
	 * @access public
	 */
	public function init() {				
		// Add Plugin actions		
		add_action( 'elementor/widgets/widgets_registered', [ $this, 'enqueue_styles' ] );	
		add_action( 'elementor/widgets/widgets_registered', [ $this, 'init_widgets' ] );	
	}


	/**
	 * Default CSS
	 *
	 * Pre-defined styling for popup
	 *
	 * @since 1.0.0
	 *
	 * @access public
	 */
	public function enqueue_styles() {
		do_action( 'elementor/editor/before_enqueue_styles' );
		wp_enqueue_style('go-cpt-filter-2c-photo-article-css');
		wp_enqueue_script('go-cpt-filter-2c-photo-article-script');
	}


	/**
	 * Init Widgets
	 *
	 * Include widgets files and register them
	 *
	 * @since 1.0.0
	 *
	 * @access public
	 */
	public function init_widgets() {

        // Include widget file        
		require_once( __DIR__ . '/widget-base.php' );

		// Register widget
		\Elementor\Plugin::instance()->widgets_manager->register_widget_type( new \GO_2C_PHOTO_ARTICLE_WIDGET() );

	}	

}
GO_2C_PHOTO_ARTICLE::instance();